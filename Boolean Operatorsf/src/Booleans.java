
public class Booleans {
	public static boolean x;
	public static boolean y;
	public static void main(String[] args) {
		
		x = false;
		y = false;
		
		if(x) { //x is true
			System.out.println("X is true");
		} else if (y) { //y is true
			System.out.println("Y is true");
		} else {
			System.out.println("Neither are true");
		}
		
	}
}
