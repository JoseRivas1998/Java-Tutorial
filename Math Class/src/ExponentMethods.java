
public class ExponentMethods {
	public static void main(String[] args) {
		System.out.println(Math.exp(1)); //returns 2.71828
		System.out.println(Math.log(Math.E)); //returns 1.0
		System.out.println(Math.log10(10)); //returns 1.0
		System.out.println(Math.pow(2, 3)); //returns 8.0
		System.out.println(Math.pow(3, 2)); //returns 9.0
		System.out.println(Math.pow(3.5, 2.5)); // 22.91765
		System.out.println(Math.sqrt(4)); //returns 2.0
		System.out.println(Math.sqrt(10.5)); //3.24
	}
}
